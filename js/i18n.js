let currentLang = 'lv';

function setLang(lang) {
  const dict = translations[lang];
  if (!dict || Object.keys(dict).length === 0) return;

  currentLang = lang;
  document.documentElement.lang = lang;
  document.documentElement.dataset.lang = lang;

  // Update text content (plain text)
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key] != null) el.textContent = dict[key];
  });

  // Update HTML content (for bold, spans, etc.)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (dict[key] != null) el.innerHTML = dict[key];
  });

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (dict[key] != null) el.placeholder = dict[key];
  });

  // Update meta
  if (dict['meta.title']) document.title = dict['meta.title'];
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && dict['meta.description']) {
    metaDesc.content = dict['meta.description'];
  }

  // Update lang switcher button
  const langBtn = document.getElementById('langToggle');
  if (langBtn) langBtn.textContent = lang.toUpperCase();

  // Update active state
  document.querySelectorAll('.lang__option').forEach(btn => {
    btn.classList.toggle('lang__option--active', btn.dataset.lang === lang);
  });

  localStorage.setItem('yatra-lang', lang);
}

function getLang() {
  return currentLang;
}

function t(key) {
  return translations[currentLang]?.[key] || translations.lv[key] || key;
}
